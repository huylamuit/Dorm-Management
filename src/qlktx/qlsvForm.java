    private void LoadTableTTSV(){
        tblModelTTSV = new DefaultTableModel();
        
        String tieude[] = {"MaSV", "CCCD", "Ten", "NgSinh", "DChi", "Truong",
            "Toa", "Phong", "SDT"};
        tblModelTTSV.setColumnIdentifiers(tieude);
        
        ArrayList<SinhVien_DTO> arrsv = new ArrayList<SinhVien_DTO>();
        
        arrsv = svBLL.timTatCaSinhVien();
        tblCount = arrsv.size();
              
        toaDTO = toaBLL.timToaTheoTruongToa(MaTT);
        
        for(int i = 0; i < tblCount; i++){
            SinhVien_DTO temp = new SinhVien_DTO();            
            temp = arrsv.get(i);
            
            String masv = temp.getMaSV();
            String cccd = temp.getCCCD();
            String ten = temp.getTen();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String ngsinh = df.format(temp.getNgaySinh());
            String dchi = temp.getDiaChi();
            String truong = temp.getTruong();
            String toa = temp.getToa();
            String phong = temp.getPhong();
            String sdt = temp.getSoDT();
            
            String[] row = {masv, cccd, ten, ngsinh, dchi, truong, toa, phong, sdt};
            
            System.out.println(toaDTO.getMaT());
            if(flag_tcsv == true)
                tblModelTTSV.addRow(row);
            else{
                if(toaDTO.getMaT().equals(temp.getToa()) == true){
                    tblModelTTSV.addRow(row);
                }                
            }
        }
        tblTTSV.setModel(tblModelTTSV);
    }
    
