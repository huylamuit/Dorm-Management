    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int indexTB = tblTTSV.getSelectedRow();
        if(indexTB >= 0 && indexTB <= tblModelTTSV.getRowCount()){
            String MaSV = tblModelTTSV.getValueAt(indexTB, 0).toString();
            int choice = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn xóa dữ liệu?",
                        "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION){
                try{
                    int result = svBLL.xoaSinhVien(MaSV);
                    if(result != 0){
                        LoadTableTTSV();
                        JOptionPane.showMessageDialog(null, "Xóa dữ liệu thành công",
                                "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                }catch(Exception ex){
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

