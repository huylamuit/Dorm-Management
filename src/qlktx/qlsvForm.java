    private void TimSV(){
        if(txtSearch.getText().equals("") == true){
            LoadTableTTSV();
        }else{
            tblModelTTSV.setRowCount(0);
            String name = txtSearch.getText();
            ArrayList<SinhVien_DTO> arrsv = svBLL.timTheoTen(name);

            for(int i = 0; i < arrsv.size(); i++){
                SinhVien_DTO temp = new SinhVien_DTO();

                temp = arrsv.get(i);
                String masv = temp.getMaSV();
                String cccd = temp.getCCCD();
                String ten = temp.getTen();
                DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                String ngsinh = df.format(temp.getNgaySinh());
                String dchi = temp.getDiaChi();
                String truong = temp.getTruong();
                String toa = temp.getToa();
                String phong = temp.getPhong();
                String sdt = temp.getSoDT();

                String[] row = {masv, cccd, ten, ngsinh, dchi, truong, toa, phong, sdt};
                tblModelTTSV.addRow(row);
            }
            tblTTSV.setModel(tblModelTTSV);
        }
    }
    
